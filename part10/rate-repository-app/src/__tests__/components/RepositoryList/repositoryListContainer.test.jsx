import { render, screen, within } from '@testing-library/react-native';
import { RepositoryListContainer } from '../../../components/RepositoryList';
import { formatCount } from '../../../components/RepositoryList/CountList';

describe('RepositoryList', () => {
  describe('RepositoryListContainer', () => {
    it('renders repository information correctly', () => {
      const repositories = {
        totalCount: 8,
        pageInfo: {
          hasNextPage: true,
          endCursor:
            'WyJhc3luYy1saWJyYXJ5LnJlYWN0LWFzeW5jIiwxNTg4NjU2NzUwMDc2XQ==',
          startCursor: 'WyJqYXJlZHBhbG1lci5mb3JtaWsiLDE1ODg2NjAzNTAwNzZd',
        },
        edges: [
          {
            node: {
              id: 'jaredpalmer.formik',
              fullName: 'jaredpalmer/formik',
              description: 'Build forms in React, without the tears',
              language: 'TypeScript',
              forksCount: 1619,
              stargazersCount: 21856,
              ratingAverage: 88,
              reviewCount: 3,
              ownerAvatarUrl:
                'https://avatars2.githubusercontent.com/u/4060187?v=4',
            },
            cursor: 'WyJqYXJlZHBhbG1lci5mb3JtaWsiLDE1ODg2NjAzNTAwNzZd',
          },
          {
            node: {
              id: 'async-library.react-async',
              fullName: 'async-library/react-async',
              description: 'Flexible promise-based React data loader',
              language: 'JavaScript',
              forksCount: 69,
              stargazersCount: 1760,
              ratingAverage: 72,
              reviewCount: 3,
              ownerAvatarUrl:
                'https://avatars1.githubusercontent.com/u/54310907?v=4',
            },
            cursor:
              'WyJhc3luYy1saWJyYXJ5LnJlYWN0LWFzeW5jIiwxNTg4NjU2NzUwMDc2XQ==',
          },
        ],
      };

      render(<RepositoryListContainer data={{ repositories }} />);

      const repositoryItems = screen.getAllByTestId('repositoryItem');

      expect(repositoryItems).toHaveLength(2);

      repositoryItems.forEach((repositoryItem, index) => {
        Object.entries(repositories.edges[index].node)
          .filter(([key]) => key !== 'id' && key !== 'ownerAvatarUrl')
          .forEach(([_key, value]) => {
            if (typeof value === 'number') {
              expect(
                within(repositoryItem).getByText(formatCount(value))
              ).not.toBeNull();
            } else {
              expect(within(repositoryItem).getByText(value)).not.toBeNull();
            }
          });
      });
    });
  });
});
